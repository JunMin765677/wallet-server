// 4. 主執行函式
async function main() {
  console.log(`開始植入 VCTemplates...`);
  for (const t of templatesData) {
    const template = await prisma.vCTemplate.upsert({
      where: { id: t.id },
      update: {}, // 如果已存在，不做任何更新
      create: t,
    });
    console.log(`Created/Found template: ${template.templateName}`);
  }

  console.log(`開始植入 Persons...`);
  for (const p of personsData) {
    const person = await prisma.person.upsert({
      where: { id: p.id }, // 我們也可以用 @unique 的 personalId: p.personalId
      update: {},
      create: p,
    });
    console.log(`Created/Found person: ${person.name}`);
  }

  console.log(`開始植入 PersonEligibilities...`);
  for (const e of eligibilitiesData) {
    // 使用 personId_templateId 這個 @@unique 複合鍵來查詢
    const eligibility = await prisma.personEligibility.upsert({
      where: { 
        personId_templateId: {
          personId: e.personId,
          templateId: e.templateId
        }
      },
      update: {},
      create: e,
    });
    console.log(`Created/Found eligibility: Person ${eligibility.personId} -> Template ${eligibility.templateId}`);
  }

  console.log(`Seeds 植入完成。`);
}

// 5. 執行並處理錯誤
main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    // 確保 Prisma Client 連線被關閉
    await prisma.$disconnect();
  });